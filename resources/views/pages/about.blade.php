			YouthBank oferă tinerilor un cadru în care ei să lucreze împreună și să ia decizii privind comunitatea din care fac parte. Anual în România în medie 15 tineri din fiecare oraș în care este implementat programul YouthBank cu vârste între 15 și 20 ani, cunoscuți ca “cei mai tineri bancheri din România” constituie, promovează și administrează transparent un fond special destinat finanțării și implementării proiectelor de către liceenii. Este un mecanism prin care sunt încurajate comportamente ale tinerilor legate de: filantropie, inițiativă civică și responsabilitate.		
			</article>

			<article style= "height: 200px; background-color:#e8edf4;text-align:center; padding:50px; font-family: Century Gothic;">
